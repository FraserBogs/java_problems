package az.eolymp;

import java.util.Scanner;

public class Problem9918 {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();
        String a = sc.next();

        for (int i = 0; i < n; i++) {
            System.out.print(a + " ");
        }
    }
}